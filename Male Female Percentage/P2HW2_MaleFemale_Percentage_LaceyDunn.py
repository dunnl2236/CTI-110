# get male input
males=int(input("How many males:"))

# get female input
females=int(input("How many females:"))

# answer*males and answer*females

total=males+females

# calculate percentage of males

malePercentage=males/total

# calculate percentage of females

femalePercentage=females/total

# display male and female in percentage

print("Total Percentage of Males:", format(malePercentage, '.0%'),\
      "Total Percentage of Females:", format(femalePercentage, '.0%'))
      
 





















    
